//package 1 Java fundamentals;

public class _4_Increment {
    public static void main(String[] args) {
        int a = 10;
        System.out.println(a);
        a = a + 1;
        System.out.println(a);
        ++a;
        System.out.println(a);
        a++;
        System.out.println(a);
        a--; // a = a - 1
        System.out.println(a);
        --a;
        System.out.println(a);
    }
}
